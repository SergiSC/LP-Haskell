------------------------------------------------------------------------------------
-------------------------------------- INSERT --------------------------------------
------------------------------------------------------------------------------------
insert :: [Integer] -> Integer -> [Integer]
insert [] x = [x]
insert (head:tail) x
    | head > x = x:head:tail
    | otherwise = head:(insert tail x)

------------------------------------------------------------------------------------
-------------------------------------- ISORT ---------------------------------------
------------------------------------------------------------------------------------
isort :: [Integer] -> [Integer]
isort (sorted:(head:tail)) = sorted:(insert tail head)
